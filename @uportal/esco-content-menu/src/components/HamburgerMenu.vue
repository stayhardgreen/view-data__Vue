<template>
  <div class="hamburger-menu">
    <div
      class="content-menu-toggle"
      aria-label="Menu"
      role="button"
      title="Menu"
      aria-expanded="false"
      aria-haspopup="true"
      aria-controls="content-menu"
      @click="toggleMenu($event)">
      <div class="menu-wrapper">
        <div />
        <div />
        <div />
      </div>
    </div>

    <content-menu
      v-if="append"
      :call-on-close="toggleMenu"
      :default-org-logo="defaultOrgLogo"
      :is-hidden="!visible"
      :user-info-portlet-url="userInfoPortletUrl"
      default-class="toggler-menu"
      visible-class="active-menu" />

  </div>
</template>

<script>
import ContentMenu from './ContentMenu';

export default {
  name: 'HamburgerMenu',
  components: {
    ContentMenu,
  },
  props: {
    contextApiUrl: {
      type: String,
      default: process.env.VUE_APP_PORTAL_CONTEXT,
    },
    signOutUrl: {type: String, default: process.env.VUE_APP_LOGOUT_URL},
    defaultOrgLogo: {type: String, required: true},
    userInfoPortletUrl: {type: String, default: ''},
  },
  data() {
    return {
      visible: false,
      append: false,
    };
  },
  mounted() {
    this.$nextTick(function() {
      this.append = true;
    });
  },
  methods: {
    toggleMenu(event) {
      event.preventDefault();
      this.visible = !this.visible;
      const html = document.querySelector('html');
      if (html) {
        html.style.overflowY = this.visible ? 'hidden' : 'auto';
      }
    },
  },
};
</script>

<style lang="scss" scoped>
.hamburger-menu {
  > .content-menu-toggle {
    color: #fff;
    text-decoration: none;
    cursor: pointer;

    &:hover > .menu-wrapper > div {
      opacity: 0.7;
    }

    > .menu-wrapper {
      width: 25px;
      padding: 0 4px;

      > div {
        height: 2px;
        background-color: #fff;
        margin: 5px 0;
      }
    }
  }

  .toggler-menu {
    position: fixed;
    width: 100%;
    height: 100%;
    top: 0;
    left: 0;
    overflow-y: scroll;
    visibility: hidden;
    opacity: 0;
    transition: opacity 600ms, visibility 600ms;
    animation: fade 600ms;
  }

  .active-menu {
    visibility: visible;
    opacity: 1;
  }
}
</style>
